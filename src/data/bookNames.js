/**
 * Get the translated book name for a given English book name and Bible version.
 */
export function getTranslatedBookName(englishBookName, version) {
    if (version === 'TB') {
        return englishToIndonesianBooks[englishBookName] || englishBookName;
    }
    if (['CNVS', 'CUNPSS-上帝', 'CUNPSS-神'].includes(version)) {
        return englishToChineseBooks[englishBookName] || englishBookName;
    }
    if (version === 'CUV') {
        return englishToTraditionalChineseBooks[englishBookName] || englishBookName;
    }
    return englishBookName;
}
